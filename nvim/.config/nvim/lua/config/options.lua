-- WSL clipboard support via win32yank
-- On macOS, Neovim auto-detects pbcopy/pbpaste
-- On native Linux, Neovim auto-detects xclip/xsel/wl-copy
if vim.fn.has("wsl") == 1 then
  vim.g.clipboard = {
    name = "win32yank-wsl",
    copy = {
      ["+"] = "win32yank.exe -i --crlf",
      ["*"] = "win32yank.exe -i --crlf",
    },
    paste = {
      ["+"] = "win32yank.exe -o --lf",
      ["*"] = "win32yank.exe -o --lf",
    },
    cache_enabled = 0,
  }
end
